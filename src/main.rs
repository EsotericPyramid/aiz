fn main() {
    let mut processed_words = Vec::new();
    let real_words = fs::read_to_string("wordlist.txt").expect("Couldn't read the file");
    let mut frequency_array = [0; 29];
    for real_word in real_words.lines() {
        frequency_array[real_word.len()] += 1;
        let mut word_vec = Vec::new();
        let mut adj_real_word = String::from(real_word);
        while adj_real_word.len() < 28 {
            adj_real_word.push_str(" ");
        }
        for letter in adj_real_word.chars() {
            for index in letter_to_one_hot(letter){
                word_vec.push(index);
            }
        }
        processed_words.push((word_vec,vec![1.0,0.0]))
    }

    println!("real words processed!");

    let letters = ["a","b","c","d","e","f","g","h","i","j","k","l","m","n","o","p","q","r","s","t","u","v","w","x","y","z"];
    let mut rng = rand::thread_rng();
    let mut fake_words = Vec::new();
    for length in 1..29 {
        for _ in 0..frequency_array[length] {
            let mut new_word = String::new();
            for _ in 0..length {
                new_word.push_str(letters.choose(&mut rng).unwrap());
            }
            for _ in 0..(28-length) {
                new_word.push_str(" ");
            }
            fake_words.push(new_word);
        }
    }

    println!("fake words created!");

    for fake_word in fake_words {
        let mut word_vec = Vec::new();
        for letter in fake_word.chars() {
            for index in letter_to_one_hot(letter){
                word_vec.push(index);
            }
        }
        processed_words.push((word_vec,vec![0.0,1.0]))
    }

    println!("fake words processed!");

    processed_words.shuffle(&mut rng);
    let mut training_data = Vec::new();
    let mut test_data = Vec::new();
    for (index,example) in processed_words.into_iter().enumerate() {
        if index < 10000 {
            test_data.push(example)
        } else {
            training_data.push(example)
        }
    }

    println!("All examples processed and split up!");

    let mut is_word_neural_network = aiz::NeuralNetwork::new(vec![728,100,100,2]);
    println!("Beginning Training!!!");
    is_word_neural_network.stochastic_back_propagation( &training_data, &test_data, (-100.0_f64).exp2(), 10000);
}